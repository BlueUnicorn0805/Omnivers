package hawaiiappbuilders.omniversapp.model;

public class Budget {

    private String imageId,title,sumOfSpent,Budget;

    public Budget() {
    }

    public String getImageId() {
        return imageId;
    }

    public void setImageId(String imageId) {
        this.imageId = imageId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSumOfSpent() {
        return sumOfSpent;
    }

    public void setSumOfSpent(String sumOfSpent) {
        this.sumOfSpent = sumOfSpent;
    }

    public String getBudget() {
        return Budget;
    }

    public void setBudget(String budget) {
        Budget = budget;
    }
}
